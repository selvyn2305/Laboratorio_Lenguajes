public class CaracterNoReconocido {
    
    private char caracter;
    private int fila;
    private int columna;

    public CaracterNoReconocido(char caracter, int fila, int columna) {
        this.caracter = caracter;
        this.fila = fila;
        this.columna = columna;
    }

    public char getCaracter() {
        return caracter;
    }

    public int getFila() {
        return fila;
    }

    public int getColumna() {
        return columna;
    }
    
    
    
}
